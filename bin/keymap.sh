# getting the current keymap
KEYMAP=$(setxkbmap -v \
    | grep symbols \
    | cut -d '+' -f 2)

# setting the keymap
if [ $KEYMAP = "se" ]; then
    setxkbmap us
    notify-send "US keymap"
else
    setxkbmap se
    notify-send "SE keymap"
fi
