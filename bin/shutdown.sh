ANS="$(echo -e "Lock \nSuspend \nSign out \nReboot \nShutdown" | dmenu -c -i -l 10)"

case "$ANS" in
    'Lock ') dm-tool lock ;;
    'Suspend ') systemctl suspend ;;
    'Sign out ') systemctl restart lightdm ;;
    'Reboot ') reboot ;;
    'Shutdown') shutdown now ;;
esac
